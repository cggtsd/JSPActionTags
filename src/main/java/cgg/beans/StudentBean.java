package cgg.beans;

public class StudentBean {
	
	//student properties
		private String name;
		private String rollNo;
	 
		//no-argument constructor
		public StudentBean(){
	 
		}
	 
		public String getName() {
			return name;
		}
	 
		public void setName(String name) {
			this.name = name;
		}
	 
		public String getRollNo() {
			return rollNo;
		}
	 
		public void setRollNo(String rollNo) {
			this.rollNo = rollNo;
		}	

}
